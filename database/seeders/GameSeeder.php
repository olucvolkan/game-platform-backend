    /**
     * Run the database seeds.
     */
    public function run(): void
    {
        $genres = Genre::all()->keyBy('slug');

        $games = $this->getGamesData();

        foreach ($games as $gameData) {
            $game = Game::create([
                'slug' => $gameData['slug'],
                'title' => $gameData['title'],
                'image' => $gameData['image'],
                'price' => $gameData['price'],
                'original_price' => $gameData['originalPrice'],
                'discount' => $gameData['discount'],
                'platform' => $gameData['platform'],
                'region' => $gameData['region'],
                'product_type' => $gameData['productType'],
                'has_cashback' => $gameData['hasCashback'],
                'cashback_percent' => $gameData['cashbackPercent'],
                'release_date' => $gameData['releaseDate'] ?? null,
                'developer' => $gameData['developer'] ?? null,
                'publisher' => $gameData['publisher'] ?? null,
                'description' => $gameData['description'] ?? null,
                'popularity_score' => $gameData['popularityScore'],
            ]);

            // Attach genres
            if (!empty($gameData['genres'])) {
                $genreIds = collect($gameData['genres'])
                    ->map(fn($slug) => $genres->get($slug)?->id)
                    ->filter()
                    ->toArray();
                $game->genres()->attach($genreIds);
            }

            // Add screenshots
            if (!empty($gameData['screenshots'])) {
                foreach ($gameData['screenshots'] as $order => $url) {
                    $game->screenshots()->create([
                        'url' => $url,
                        'order' => $order,
                    ]);
                }
            }
        }
    }

